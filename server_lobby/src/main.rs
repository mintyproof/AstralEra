#[derive(Debug, PartialEq, DekuRead, DekuWrite)]
#[deku(type = "u16")]
enum PacketConnectionType {
    #[deku(id = "0x00")]
    Unknown,
    #[deku(id = "0x01")]
    Zone,
    #[deku(id = "0x02")]
    Chat,
    #[deku(id = "0x03")]
    LobbyHandshake // maybe??
}

#[derive(Debug, PartialEq, DekuRead, DekuWrite)]
struct PacketHeader {
    /// is this packet authenticated?
    pub is_authenticated: bool,
    /// indicates whether the packet data is compressed or encrypted:
    /// - when talking to the lobby server, if this is true, the data is encrypted with Blowfish.
    /// - when talking to world / level servers, if this is true, the data is compressed with zlib.
    pub is_compressed_or_encrypted: bool,
    /// (this may be incorrect. pending investigation)
    /// 
    /// indicates the connection type of the packet on initial handshake-
    /// whether we're dealing with a zone, chat, lobby setup, etc.
    pub connection_type: PacketConnectionType,
    /// the size of the packet, *including* this header.
    pub packet_size: u16,
    /// how many subpackets are in this packet?
    pub subpacket_count: u16,
    /// timestamp in milliseconds.
    pub timestamp: u64
}

#[derive(Debug, PartialEq)]
struct Packet {
    pub header: PacketHeader
}

impl Packet {
    fn from_bytes(bytes: &[u8]) -> Result<(), ()> {
        Ok(())
    }

    fn with_subpacket(subpacket: &Subpacket) -> Result<(), ()> {
        Ok(())
    }

    fn with_subpackets(subpackets: &[Subpacket]) -> Result<(), ()> {
        Ok(())
    }
}

#[derive(Debug, PartialEq, DekuRead, DekuWrite)]
struct SubpacketHeader {
    /// the size of the subpacket, *including* this header.
    subpacket_size: u16,
    /// .. docs todo
    subpacket_type: u16,
    /// the actor that caused this subpacket to be sent.
    source_id: u32,
    /// the actor this subpacket is targeting.
    target_id: u32,
    /// ?
    unknown: u32
}

#[derive(Debug, PartialEq)]
struct Subpacket {
    pub header: SubpacketHeader
}

fn handle_new_connection(mut stream: TcpStream) {
    let mut buffer = [0; 1024];
    stream.read(&mut buffer).unwrap();

    let (rest, packet) = PacketHeader::from_bytes((buffer.as_ref(), 0)).unwrap();
    let (rest, _) = rest;
    let (_, subpacket) = SubpacketHeader::from_bytes((rest.as_ref(), 0)).unwrap();
    println!("{:?}", packet);
    println!("{:?}", subpacket);

    let key_phrase = String::from_utf8_lossy(&rest[0x34..0x74]);
    let key_phrase = key_phrase.trim_matches(char::from(0));
    let key_number = LittleEndian::read_u32(&rest[0x74..0x78]);

    println!("{}", key_phrase);
    println!("{}", key_number);
}

fn main() {
    let listener = TcpListener::bind("127.0.0.1:54994").unwrap();
    println!("server ready!");
    
    for stream in listener.incoming() {
        let stream = stream.unwrap();
        handle_new_connection(stream);
    }
}